package packge;

public class Swap_no1 
{

	public static void main(String[] args) 
	{
		int num1=123;
		int num2=852;
		int swap;
		System.out.print(num1);
		System.out.println(" "+num2);
		swap=num1;
		num1=num2;
		num2=swap;
		System.out.print(num1);
		System.out.println(" "+num2);
		

	}

}
